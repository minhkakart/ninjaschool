    public static BufferedImage[] convertStringToImageArray(String string, StringFont stringFont) {
        BufferedImage[] bufferedImages = new BufferedImage[string.length()];
        for (int i = 0; i < string.length(); i++) {
            Rectangle charRect = getCharRect(string.charAt(i), stringFont.getFontType());
            bufferedImages[i] = ResourceManager.getStringFont(stringFont).getSubimage(charRect.x, charRect.y, charRect.width, charRect.height);
        }
        
        return bufferedImages;
    }
    
    public static Rectangle getCharRect(Character character, FontType fontType) {
        Map<Character, Rectangle> charMap;
        switch (fontType) {
            case FONT_7B:
                charMap = CHAR_MAP_7B;
                break;
            case FONT_8B:
                charMap = CHAR_MAP_8B;
                break;
            case FONT_7A:
            default:
                charMap = CHAR_MAP_7A;
        }
        
        Rectangle charRect = charMap.get(character);
        if (charRect == null) {
            charRect = charMap.get(' ');
        }
        
        return charRect;
    }
